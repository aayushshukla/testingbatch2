package com.infosys;

public class CalculatorUtil {

    public int add(int a , int b)
    {
        return a+b;
    }

    public int multiple(int a , int b)
    {
        return a*b;
    }

    public  int div(int a , int b)
    {
        return  a/b;
    }

    public  int multiple(int a, int b , int c)
    {
        return  a*b*c;
    }


    public boolean checkEvenNo(int n)
    {
        return  n%2==0;
    }



}
